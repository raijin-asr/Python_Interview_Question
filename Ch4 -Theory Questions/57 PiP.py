# What is PIP?
# PIP, a recursive acronym for Pip Installs Packages, is a tool that comes with Python and is used to manage packages in our system.

# We can install, uninstall, upgrade packages, and do various other things using pip. Pip searches the package at https://pypi.org and installs the packages.

# For example, if we want to install the Django framework in our system, we can run,

# pip install django
# in our terminal.

# Also, we can view all the installed packages in our system using the

# pip freeze
# command.