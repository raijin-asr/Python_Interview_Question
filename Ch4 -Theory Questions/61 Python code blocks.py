"""What Are Code Blocks in Python? How Do You Create Them in Python?
Code blocks are a group of statements that perform some task. For example, a group of statements inside the if statement is a code block. Also, codes within a function are another code block.

In Python, we create them by indenting lines inside the parent statement followed by the colon (:).

Let's take a look at an example,"""

if True:
    print('Code Block 1')
 
def main():
    print('Code Block 2')
 
main()

# There are two code blocks in the above code: one inside the if condition and the other inside the main() function.

