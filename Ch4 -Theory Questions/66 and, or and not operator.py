"""Explain and, or & not Operators in Python
The and, or, and not are boolean operators in Python. They are used to compare two boolean values.

and operator
The and operator returns True if both compared values are True. In any other case, the and operator returns False. For example,"""

print(True and True)  # True
print(True and False)  # False
print(False and True)  # False
print(False and False)  # False


# or operator
# The or operator returns True if any of the compared values are True, and only returns False if both compared values are False. For example,

print(True or True)  # True
print(True or False)  # True
print(False or True)  # True
print(False or False)  # False


# not operator
# The not operator inverts the boolean value i.e. the True value is inverted to False and vice versa. For example,
print(not True)  # False
print(not False)  # True
